package encriptacion;

/**
 *
 * @author Pato
 */
public class ArchivoDesencriptado {
    private String ruta;
    private String textoDesencriptado;

    /**
     * @return the ruta
     */
    public String getRuta() {
        return ruta;
    }

    /**
     * @param ruta the ruta to set
     */
    public void setRuta(String ruta) {
        this.ruta = ruta;
    }

    /**
     * @return the textoDesencriptado
     */
    public String getTextoDesencriptado() {
        return textoDesencriptado;
    }

    /**
     * @param textoDesencriptado the textoDesencriptado to set
     */
    public void setTextoDesencriptado(String textoDesencriptado) {
        this.textoDesencriptado = textoDesencriptado;
    }
}
